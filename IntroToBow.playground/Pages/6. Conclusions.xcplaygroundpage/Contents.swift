 ## Conclusions
 
 Bow is a library for Typed Functional Programming in Swift. This playground has only scratched the surface of what it has to offer. There are many other features that haven't been explored:
 - More advanced Data Types
 - Free Monads
 - Monad Transformers
 - Optics
 - Recursion Schemes
 
 ### Advantages
 
 Some of the benefits of using Bow and Functional Programming are:
 - Code easier to reason
 - Maintainability
 - Testability
 - Reusability
 
 ### Drawbacks
 
 Bow is still in early stage of development. It has been thoroughly tested but needs to be applied into some projects to evaluate its usability. The library pushes the language limits to an extent where some concepts are not possible to be written, or others involve an amount of boilerplate which makes it very annoying to use it. We hope that everyone, as a community, helps to move this project forward and to eliminate the barriers that hinder the usage of Bow.
 
